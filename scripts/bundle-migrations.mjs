function main() {
  if (!existsSync(migrationsDir)) {
    throw new Error("Missing supabase/migrations directory");
  }

  const files = readdirSync(migrationsDir).filter((name) => name.endsWith(".sql")).sort();
  const missing = expected.filter((name) => !files.includes(name));
  const extra = files.filter((name) => !expected.includes(name));

  if (missing.length > 0) {
    throw new Error(`Missing migration files: ${missing.join(", ")}`);
  }

  const chunks = [
    "-- Zigo full migration bundle",
    "-- Generated by npm run migrations:bundle",
    "-- Paste into Supabase SQL Editor as one run, or apply files individually in order.",
    "",
  ];

  for (const fileName of expected) {
    const filePath = join(migrationsDir, fileName);
    const sql = readFileSync(filePath, "utf8").trim();
    chunks.push(`-- >>> ${fileName}`);
    chunks.push(sql);
    chunks.push("");
  }

  writeFileSync(outputPath, `${chunks.join("\n")}\n`, "utf8");

  console.log(`PASS Bundled ${expected.length} migrations`);
  console.log(`Output: ${outputPath}`);
  if (extra.length > 0) {
    console.log(`NOTE Extra migration files not bundled: ${extra.join(", ")}`);
  }
}

main();
